export const setMembers = (member) => {
  return {
    type: 'SET_MEMBERS',
    payload: member
  }
}
export const setFlickr = (flickr) => {
  return {
    type: 'SET_FLICKR',
    payload: flickr
  }
}
export const setYoutube = (youtube) => {
  return {
    type: 'SET_YOUTUBE',
    payload: youtube
  }
}